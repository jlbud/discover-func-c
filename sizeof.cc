#include <iostream>
using namespace std;

int main()
{
    cout << "Size of char: " << sizeof(char) << " byte" << endl;
    cout << "Size of char: " << sizeof(int) << " bytes" << endl;
    cout << "Size of char: " << sizeof(float) << " bytes" << endl;
    cout << "Size of char: " << sizeof(double) << " bytes" << endl;
    return 0;
}